const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "../..");
const canonicalTokensPath = path.join(rootDir, "canonical/tokens.json");
const distTypesDir = path.join(rootDir, "dist/themes/rei-dot-com/types");
const distMetaDir = path.join(rootDir, "dist/themes/rei-dot-com/meta");
const legacyThemeRootDir = path.join(rootDir, "dist/rei-dot-com");
const distCoreTypesDir = path.join(rootDir, "dist/types/core");
const distPublicTypesDir = path.join(rootDir, "dist/types");

type ModuleFileOptions = {
  module: ModuleDefinition;
  interfaceName?: string;
  unionTypeName?: string;
};

interface TokenLeaf {
  $value: string | number | boolean;
  $type: string;
  $extensions?: {
    cedar?: {
      docs?: TokenDocumentation;
    };
  };
}

interface TokenDocumentation {
  summary?: string;
  usage?: string;
  design?: string;
  aliases?: string[];
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null;
}

function isTokenLeaf(value: unknown): value is TokenLeaf {
  return isRecord(value) && "$value" in value && "$type" in value;
}

function toPascalCase(value: string): string {
  const parts = value.match(/[a-zA-Z0-9]+/g) ?? [];
  return parts
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1).toLowerCase())
    .join("");
}

function writeFile(filePath: string, content: string): void {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content);
}

function toDocsTypeName(moduleFileName: string): string {
  return `${toPascalCase(moduleFileName)}TokenDocs`;
}

function collectLeafNodes(
  node: unknown,
  currentPath: string[] = [],
): Array<{ path: string[]; token: TokenLeaf }> {
  if (isTokenLeaf(node)) {
    return [{ path: currentPath, token: node }];
  }

  if (!isRecord(node)) {
    return [];
  }

  return Object.entries(node).flatMap(([key, child]) => {
    if (key.startsWith("$")) return [];
    return collectLeafNodes(child, [...currentPath, key]);
  });
}

function normalizeDocs(docs: unknown): TokenDocumentation | undefined {
  if (!isRecord(docs)) return undefined;

  const result: TokenDocumentation = {};
  if (typeof docs.summary === "string" && docs.summary.trim()) result.summary = docs.summary;
  if (typeof docs.usage === "string" && docs.usage.trim()) result.usage = docs.usage;
  if (typeof docs.design === "string" && docs.design.trim()) result.design = docs.design;
  if (Array.isArray(docs.aliases)) {
    const aliases = docs.aliases.filter((entry): entry is string => typeof entry === "string");
    if (aliases.length > 0) result.aliases = aliases;
  }

  return Object.keys(result).length > 0 ? result : undefined;
}

function writeCoreTypeContracts(modules: ModuleDefinition[]): string[] {
  const generatedFiles: string[] = [];
  const moduleNames = [
    ...new Set(modules.map((moduleDefinition) => moduleDefinition.moduleFileName)),
  ].sort((left, right) => left.localeCompare(right));

  const moduleUnionBody =
    moduleNames.length === 0
      ? "never"
      : moduleNames.map((moduleName) => `  | ${JSON.stringify(moduleName)}`).join("\n");

  const systemPath = path.join(distCoreTypesDir, "system.d.ts");
  writeFile(
    systemPath,
    [
      "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
      "",
      'export type Theme = "rei-dot-com";',
      'export type Platform = "web" | "ios" | "android";',
      'export type Responsibility = "foundations";',
      "",
      "export type FoundationModule =",
      `${moduleUnionBody};`,
      "",
    ].join("\n"),
  );
  generatedFiles.push(systemPath);

  const dictionaryPath = path.join(distCoreTypesDir, "token-dictionary.d.ts");
  writeFile(
    dictionaryPath,
    [
      "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
      "",
      'import type { Theme, Platform, Responsibility, FoundationModule } from "./system";',
      "",
      "export type TTheme = Theme;",
      "export type TPlatform = Platform;",
      "export type TResponsibility = Responsibility;",
      "export type TModule = FoundationModule;",
      "",
      "export interface TokenDocumentation {",
      "  summary?: string;",
      "  usage?: string;",
      "  design?: string;",
      "  aliases?: string[];",
      "}",
      "",
      "export type TokenMap = Record<string, string>;",
      "",
      "export type TokenDocsMap<TTokenName extends string = string> = Partial<",
      "  Record<TTokenName, TokenDocumentation>",
      ">;",
      "",
      "export interface TokenDictionary<",
      "  TTheme extends Theme,",
      "  TPlatform extends Platform,",
      "  TResponsibility extends Responsibility,",
      "  TModuleName extends string,",
      "  TTokens extends TokenMap,",
      "  TTokenName extends keyof TTokens & string = keyof TTokens & string,",
      "> {",
      "  theme: TTheme;",
      "  platform: TPlatform;",
      "  responsibility: TResponsibility;",
      "  module: TModuleName;",
      "  tokens: TTokens;",
      "  docs?: TokenDocsMap<TTokenName>;",
      "}",
      "",
    ].join("\n"),
  );
  generatedFiles.push(dictionaryPath);

  const coreIndexPath = path.join(distCoreTypesDir, "index.d.ts");
  writeFile(
    coreIndexPath,
    [
      "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
      "",
      'export * from "./system";',
      'export * from "./token-dictionary";',
      "",
    ].join("\n"),
  );
  generatedFiles.push(coreIndexPath);

  return generatedFiles;
}

function writeModuleDocsArtifacts(tokens: DesignTokens, modules: ModuleDefinition[]): string[] {
  const generatedFiles: string[] = [];
  const leafNodes = collectLeafNodes(tokens);

  modules.forEach((moduleDefinition) => {
    const docsByTokenName: Record<string, TokenDocumentation> = {};

    leafNodes.forEach(({ path, token }) => {
      const transformedToken = { path } as unknown as TransformedToken;
      if (!moduleDefinition.matchesToken(transformedToken)) return;

      const docs = normalizeDocs(token.$extensions?.cedar?.docs);
      if (!docs) return;

      const tokenName = moduleDefinition.getTokenName(transformedToken);
      docsByTokenName[tokenName] = docs;
    });

    const docsJsonPath = path.join(
      distMetaDir,
      moduleDefinition.responsibility,
      `${moduleDefinition.moduleFileName}.docs.json`,
    );
    writeFile(docsJsonPath, `${JSON.stringify(docsByTokenName, null, 2)}\n`);
    generatedFiles.push(docsJsonPath);

    const docsTypePath = path.join(
      distTypesDir,
      moduleDefinition.responsibility,
      `${moduleDefinition.moduleFileName}.docs.d.ts`,
    );
    writeFile(
      docsTypePath,
      [
        "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
        "",
        `import type { TokenDocsMap } from "../../../types/core/token-dictionary";`,
        `import type { ${moduleDefinition.unionTypeName} } from "./${moduleDefinition.moduleFileName}.names";`,
        "",
        `export type ${toDocsTypeName(moduleDefinition.moduleFileName)} = TokenDocsMap<${moduleDefinition.unionTypeName}>;`,
        "",
      ].join("\n"),
    );
    generatedFiles.push(docsTypePath);
  });

  return generatedFiles;
}

function renderTokenNameUnionType(typeName: string, tokenNames: string[]): string {
  const body =
    tokenNames.length === 0
      ? "never"
      : tokenNames.map((tokenName) => `  | ${JSON.stringify(tokenName)}`).join("\n");

  return [
    "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
    "",
    `export type ${typeName} =`,
    `${body};`,
    "",
  ].join("\n");
}

function renderModuleInterfaceType(interfaceName: string, tokenNames: string[]): string {
  const properties = tokenNames
    .map((tokenName) => `  ${JSON.stringify(tokenName)}: string;`)
    .join("\n");

  return [
    "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
    "",
    `export interface ${interfaceName} {`,
    properties,
    "}",
    "",
  ].join("\n");
}

function collectModeColorAliasTokenNames(tokens: DesignTokens): Map<string, string[]> {
  const result = new Map<string, string[]>();
  const color = (tokens as Record<string, unknown>)["color"];
  if (!isRecord(color)) return result;

  const modes = color["modes"];
  if (!isRecord(modes)) return result;

  const walk = (node: unknown, currentPath: string[], out: string[]) => {
    if (isTokenLeaf(node)) {
      out.push(currentPath.join("-"));
      return;
    }

    if (!isRecord(node)) {
      return;
    }

    for (const [key, child] of Object.entries(node)) {
      if (key.startsWith("$")) continue;
      walk(child, [...currentPath, key], out);
    }
  };

  for (const [modeName, modeNode] of Object.entries(modes)) {
    if (!isRecord(modeNode)) continue;
    const names: string[] = [];
    walk(modeNode, [], names);
    names.sort((left, right) => left.localeCompare(right));
    result.set(modeName, names);
  }

  return result;
}

function writeModeColorAliasTypes(tokens: DesignTokens): string[] {
  const generatedFiles: string[] = [];
  const modeTokenNames = collectModeColorAliasTokenNames(tokens);
  const modesDir = path.join(distTypesDir, "foundations/modes");

  const modeNames = [...modeTokenNames.keys()].sort((left, right) => left.localeCompare(right));
  const cedarModeUnion =
    modeNames.length === 0
      ? "never"
      : modeNames.map((modeName) => JSON.stringify(modeName)).join(" | ");

  for (const modeName of modeNames) {
    const tokenNames = modeTokenNames.get(modeName) ?? [];
    const modeTypeName = toPascalCase(modeName);
    const unionTypeName = `CdrColorAlias${modeTypeName}TokenName`;
    const interfaceName = `CdrColorAlias${modeTypeName}Tokens`;
    const modeDir = path.join(modesDir, modeName);

    const namesPath = path.join(modeDir, "cdr-color-alias.names.d.ts");
    const interfacePath = path.join(modeDir, "cdr-color-alias.d.ts");

    writeFile(namesPath, renderTokenNameUnionType(unionTypeName, tokenNames));
    writeFile(interfacePath, renderModuleInterfaceType(interfaceName, tokenNames));

    generatedFiles.push(namesPath, interfacePath);
  }

  const modesIndexLines = [
    "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
    "",
    ...modeNames.flatMap((modeName) => [
      `export * from "./${modeName}/cdr-color-alias.names";`,
      `export * from "./${modeName}/cdr-color-alias";`,
    ]),
    "",
    `export type CedarMode = ${cedarModeUnion};`,
    "",
  ];

  const modesIndexPath = path.join(modesDir, "index.d.ts");
  writeFile(modesIndexPath, modesIndexLines.join("\n"));
  generatedFiles.push(modesIndexPath);

  return generatedFiles;
}

function writeBarrelFiles(modules: ModuleDefinition[]): string[] {
  const generatedFiles: string[] = [];
  const uniqueModules = [
    ...new Set(modules.map((moduleDefinition) => moduleDefinition.moduleFileName)),
  ];

  const foundationsIndexLines = [
    "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
    "",
    ...uniqueModules.flatMap((moduleFileName) => [
      `export * from "./${moduleFileName}.names";`,
      `export * from "./${moduleFileName}";`,
      `export * from "./${moduleFileName}.docs";`,
    ]),
    'export * from "./modes";',
    "",
  ];

  const foundationsIndexPath = path.join(distTypesDir, "foundations/index.d.ts");
  writeFile(foundationsIndexPath, foundationsIndexLines.join("\n"));
  generatedFiles.push(foundationsIndexPath);

  const rootIndexPath = path.join(distTypesDir, "index.d.ts");
  writeFile(
    rootIndexPath,
    [
      "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
      "",
      'export * from "./foundations";',
      "",
    ].join("\n"),
  );
  generatedFiles.push(rootIndexPath);

  const distTypesIndexPath = path.join(distPublicTypesDir, "index.d.ts");
  writeFile(
    distTypesIndexPath,
    [
      "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
      "",
      'export * from "./base/token-schema";',
      'export * from "./core";',
      'export * from "../themes/rei-dot-com/types";',
      "",
    ].join("\n"),
  );
  generatedFiles.push(distTypesIndexPath);

  return generatedFiles;
}

function renderTokenNameUnion({ dictionary, file }: FormatFnArguments): string {
  const { module, unionTypeName } = (file.options ?? {}) as ModuleFileOptions;
  const tokenNames = getModuleTokenNames(dictionary, module);
  const typeName = unionTypeName ?? "TokenName";

  const body =
    tokenNames.length === 0
      ? "never"
      : tokenNames.map((tokenName) => `  | ${JSON.stringify(tokenName)}`).join("\n");

  return [
    "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
    "",
    `export type ${typeName} =`,
    `${body};`,
    "",
  ].join("\n");
}

function renderModuleInterface({ dictionary, file }: FormatFnArguments): string {
  const { module, interfaceName } = (file.options ?? {}) as ModuleFileOptions;
  const tokenNames = getModuleTokenNames(dictionary, module);
  const tokensByName = getModuleTokensByName(dictionary, module);
  const typeName = interfaceName ?? "ModuleTokens";

  // Build property lines with optional JSDoc comments from $extensions.cedar.docs
  const properties = tokenNames
    .map((tokenName) => {
      const token = tokensByName.get(tokenName);

      // Render all populated doc fields as a JSDoc block:
      //   @summary   (rendered as the leading description, no tag)
      //   @usage
      //   @design
      //   @aliases
      const docs = (token as any)?.$extensions?.cedar?.docs;
      let jsdoc = "";
      if (docs) {
        const lines: string[] = [];
        if (docs.summary) lines.push(docs.summary);
        if (docs.usage) lines.push(`@usage ${docs.usage}`);
        if (docs.design) lines.push(`@design ${docs.design}`);
        if (Array.isArray(docs.aliases) && docs.aliases.length > 0) {
          lines.push(`@aliases ${docs.aliases.join(", ")}`);
        }
        if (lines.length > 0) {
          jsdoc =
            lines.length === 1
              ? `/** ${lines[0]} */\n  `
              : `/**\n   * ${lines.join("\n   * ")}\n   */\n  `;
        }
      }

      return `${jsdoc}${JSON.stringify(tokenName)}: string;`;
    })
    .join("\n  ");

  return [
    "/* This file is auto-generated by Style Dictionary. Do not edit manually. */",
    "",
    `export interface ${typeName} {`,
    `  ${properties}`,
    "}",
    "",
  ].join("\n");
}

async function main() {
  const canonicalTokens = JSON.parse(fs.readFileSync(canonicalTokensPath, "utf8")) as DesignTokens;
  const modules = collectModuleDefinitions(canonicalTokens);

  fs.rmSync(distTypesDir, { recursive: true, force: true });
  fs.rmSync(distMetaDir, { recursive: true, force: true });
  fs.rmSync(legacyThemeRootDir, { recursive: true, force: true });
  fs.rmSync(distCoreTypesDir, { recursive: true, force: true });

  const sd = new StyleDictionary({
    usesDtcg: true,
    tokens: canonicalTokens,
    hooks: {
      formats: {
        "typescript/token-name-union": renderTokenNameUnion,
        "typescript/module-interface": renderModuleInterface,
      },
    },
    platforms: {
      typescript: {
        buildPath: "dist/themes/",
        transforms: ["name/camel"],
        files: modules.flatMap((moduleDefinition) => [
          {
            destination: `${moduleDefinition.theme}/types/${moduleDefinition.responsibility}/${moduleDefinition.moduleFileName}.names.d.ts`,
            format: "typescript/token-name-union",
            filter: (token) => moduleDefinition.matchesToken(token),
            options: {
              module: moduleDefinition,
              unionTypeName: moduleDefinition.unionTypeName,
            },
          },
          {
            destination: `${moduleDefinition.theme}/types/${moduleDefinition.responsibility}/${moduleDefinition.moduleFileName}.d.ts`,
            format: "typescript/module-interface",
            filter: (token) => moduleDefinition.matchesToken(token),
            options: {
              module: moduleDefinition,
              interfaceName: moduleDefinition.interfaceName,
            },
          },
        ]),
      },
    },
  });

  await sd.buildAllPlatforms();

  const coreTypeFiles = writeCoreTypeContracts(modules);
  const docsArtifactFiles = writeModuleDocsArtifacts(canonicalTokens, modules);
  const modeFiles = writeModeColorAliasTypes(canonicalTokens);
  const barrelFiles = writeBarrelFiles(modules);

  console.log(`Generated ${modules.length} module declaration sets in ${distTypesDir}`);
  console.log(
    `Generated ${coreTypeFiles.length + docsArtifactFiles.length + modeFiles.length + barrelFiles.length} TypeScript support files (core + docs + modes + barrels).`,
  );
}

main().catch((error) => {
  console.error("Failed to build Style Dictionary TypeScript outputs:", error);
  process.exit(1);
});
